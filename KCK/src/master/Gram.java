package master;

import java.util.Hashtable;
import java.util.Scanner;


import org.jpl7.JPL;
import org.jpl7.Query;

public class Gram {

	public void Gram() {
		
		Scanner input = new Scanner(System.in);

		String t1 = "consult('gram.pl')";
		Query q1 = new Query(t1);
		System.out.println(t1 + " " + (q1.hasSolution() ? "succeeded" : "failed"));
	
		String sentence = input.nextLine();
		while (sentence != null && !sentence.isEmpty()) {
			String[] words = sentence.split(" ");
			sentence = "zdanie(X, [" + String.join(", ", words) + "], [])";
			System.out.println(Query.hasSolution(sentence) ? Query.oneSolution(sentence).get("X")
					: "Komenda nie zostala rozpoznana.");
			sentence = input.nextLine();
			
		}
		
		input.close();
	}

}
